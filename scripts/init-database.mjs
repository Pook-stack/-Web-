async function executeSQL(sql) {
  try {
    const { data, error } = await supabase.rpc('execute_sql', { sql: sql });
    if (error) {
      console.log(`❌ SQL执行失败: ${error.message}`);
      return null;
    }
    console.log(`✅ SQL执行成功`);
    return data;
  } catch (error) {
    console.log(`❌ SQL执行异常: ${error.message}`);
    return null;
  }
}

